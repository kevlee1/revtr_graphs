import matplotlib.pyplot as plt

# x axis values
X = [1, 2, 3]
# corresponding y values
Y = [2, 4, 1]

# plotting the points
plt.plot(X, Y)

# naming the x axis
plt.xlabel('x- axis')
# naming the y axis
plt.ylabel('y-axis')

# giving a title to the graph
plt.title('graph1')

# function to show the plot
plt.show()
